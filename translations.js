// Translation utility functions
let currentLanguage = 'en';

function setLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('website-language', lang);
    translatePage();
    updateLanguageSelector();
}

function getTranslation(key) {
    const translations = window.websiteTranslations[currentLanguage];
    return translations[key] || window.websiteTranslations.en[key] || key;
}

function translatePage() {
    // Update all elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        const translation = getTranslation(key);
        
        if (element.tagName === 'INPUT' && element.type === 'text') {
            element.placeholder = translation;
        } else {
            element.textContent = translation;
        }
    });
    
    // Update page title
    document.title = getTranslation('title') || 'MoodleGPT - AI-Powered Moodle Quiz Assistant';
    
    // Update HTML lang attribute
    document.documentElement.lang = currentLanguage;
}

function updateLanguageSelector() {
    const selector = document.getElementById('languageSelector');
    if (selector) {
        const options = selector.querySelectorAll('option');
        options.forEach(option => {
            if (option.value === currentLanguage) {
                option.selected = true;
            }
        });
    }
}

// Initialize language on page load
function initializeLanguage() {
    const savedLanguage = localStorage.getItem('website-language');
    const browserLanguage = navigator.language.slice(0, 2);
    
    if (savedLanguage && window.websiteTranslations[savedLanguage]) {
        currentLanguage = savedLanguage;
    } else if (window.websiteTranslations[browserLanguage]) {
        currentLanguage = browserLanguage;
    } else {
        currentLanguage = 'en';
    }
    
    translatePage();
    updateLanguageSelector();
}

// Export for use in other files
window.setLanguage = setLanguage;
window.getTranslation = getTranslation;
window.initializeLanguage = initializeLanguage;
